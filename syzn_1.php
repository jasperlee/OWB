          <h3>1. 阅读本指南重要？</h3>
          <p>《读我背单词》的各项操作非常简单直观，使用起来非常容易。只要您有一定的电脑操作经验，就能轻易地摸索出各个背单词功
            能的操作步骤。但是我们还是希望您在真正使用《读我背单词》之前，仔细阅读本操作指南。因为本操作指南不仅告诉您如何使用《
            读我背单词》，更多的是告诉您如何用它来提高背单词的效率。前人的经验会让您少走很多弯路，这些经验是我们从《读我背单词》
            的数十位试用者的几个月的试用体会中提炼出来的。</p>
   
      <p>&nbsp;</p>
      <h3>2. 为什么开发《读我背单词》？</h3>
      <h4> 2.1.1 背单词的重要性 </h4>
       <p>所有的英语学习者都知道，背单词对于英语学习多么重要 ！无论你是考四级、考六级、考研，还是考雅思、考托福等，你在英语
            学习上所花费的90%以上的时间都是在背单词。可以毫不夸张地说，语言学习本质上就是词汇学习。如果你能将常用的5000个英语单词
            掌握得非常透彻——能够准确地理解其含义且熟练地掌握其用法 ，能够不假思索且基本正确地说出其发音——那么你的英语水平就完
            全达到了可以正常使用英语的地步，并且足以应付各种英语考试。</p>
      <p>&nbsp;</p>
      <h4>2.1.2 中国英语学习者的词汇现状</h4>
      <p>词汇量很大，但对词汇的掌握程度很低。过了大学英语4级的大学生的词汇量应该在8000以上。有这么大的词汇量 ，应该说英语
        水平已经很高。但是90%以上的大学生对这些词汇的熟悉程度只是能够连蒙带猜地做词义选择题。除了临时应付简单的英语考试外，这种熟悉程度同完全不会几乎没有区别。</p>
      <p>&nbsp;</p>
      <p>根据拼写想当然地为英语单词发音。这主要表现在随意定义重音或者根本不考虑重音 。我们公司曾经面试了大量的应届大学毕业生，其中一道面试题就是让应聘者朗读20个小学单词(从小学词汇表中随机选择的)。令人非常意外的是 ：没有一人能把这20个单词能
        基本读正确。主要原因就是重音错误。</p>
      <p>&nbsp;</p>
      <h4>2.1.3 现有背单词软件的功能缺陷 </h4>
      <p>既然背单词如此重要，背单词的软件产品自然非常多（目前估计有数百种之多）。但现有的背单词软件主要是针对有临时快速记
        忆需求的用户（这类用户很多）。它们主要通过词义练习和拼写练习让你对想记忆的单词有些印象 。这种方法可能能让你临时应付简
        单的考试，但不可能让你真正记牢单词。记得快，忘得更快。</p>
      <p>&nbsp;</p>
      <h4>2.1.4 矫正单词发音错误是《读我背单词》的最初开发动机 </h4>
      <p>公司成立之初，我们曾经组织过一个20多人参加的小型英语学习者研讨会。会上大家有两个让所有学习者产生共鸣的问题：<br />
        a.单词的重音位置总是搞混；<br />
        b.习惯性发音错误总是改不掉。于是我们决定从这两个问题入手开发一个背单词软件。</p>
      <p>&nbsp;</p>
      <h4>2.1.5 长达半年的试用来完善《读我背单词》 </h4>
      <p>讨论会后，我们立即针对英语学习者的主要问题开发完成了一个背单词的原型产品，其功能非常单一，就是捋发音。因为我们认为功能越单一的产品，其卖点越明确。后来经过试用者反复试用，反复提建议，反复修改，才变成了现在的功能相对完备的《读我背单词》。</p>
      <p>a. 有的试用者提出：用&quot;捋&quot;的方式背单词虽然很好，能够把半生不熟的单词记牢，但不适于记忆生单词。于是，我们增加了背生词的功能。</p>
      <p>b. 有些试用者认为其他背单词产品的功能也很有用，于是我们增加了词义练习和拼写练习。</p>
      <p>c. 有些试用者认为朗读单词功能虽然很好，但是对于某些生单词，一开始就朗读，难度很大。于是，我们增加了跟读练习。</p>
      <p>d. 有很多试用者还提出了很多很好的建议，但是我们无法把它们全部放在《读我背单词》中，我们将在以后的产品中实现。</p>
