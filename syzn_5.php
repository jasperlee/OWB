          <h4>3.7 朗读练习的步骤</h4>
    <p> 朗读练习和其他练习的区别是朗读练习包含两个环节：朗读单词和判别正误。<br />
</p>
      
      <p>a. 重音位置是否正确</p>
      <p>b. 重音读的是否正确</p>
      <p>c. 音节数是否正确</p>
      <p>d. 各个非重读音节是否基本正确</p>
      <p> 您的母语不是英语，如果您不认为您是语言天才，您不必也不需要对您的发音过份挑剔。您练习发音的目的应该是：您能听懂别<br />
</p>
      <p>&nbsp;</p>

      <h4> 3.8 如何朗读不大熟悉的单词？</h4>
       <p>a. 面对不熟悉的单词，不要急于开口。</p>
       <p>b. 首先要想一想这个单词包含哪几个音节，其次要确定哪一个是重读音节，然后想想各个音节的发音，最后把所有音节连起来读。</p>
       <p>c. 朗读不熟悉的单词不能求快，一定要把每个音读清楚。</p>

<p>&nbsp;</p>
      <h4>3.9 试用课程</h4>
      <p>我们设置&quot;试用课程&quot;有两个目的：</p>
      <p>a. 是让所有人都能了解《读我背单词》的功能；</p>
      <p>b. 是让用户能够学会《读我背单词》的各个功能的操作步骤。</p>
<p>当您针对试用课程进行背单词练习时，在进入任何一个功能界面时，都会有非常详细的使用提示。如果您对某些功能有任何疑问，<br />
</p>
      <p>&nbsp;</p>
      <h4>3.10 我的单词库</h4>
      <p>&quot;我的单词库&quot;是一个功能性课程，位于课程树的前面。您可以使用它来背诵您希望专门背诵的单词。在使用前，您需要自己把<br />
&quot;我的单词库&quot;添加单词。</p>
      <p>a. 是通过背单词的词汇选择功能。</p>
      <p>b. 是查单词功能。</p>
      <p> 添加好单词后，您就可以象其他课程一样，利用《读我背单词》的全部功能来背诵&quot;我的单词库&quot;中的单词。<br />
在进入&quot;我的单词库&quot;后，您可以选择删除其中的单词。当您把以前的单词背诵完成后，可以更换新的单词进入&quot;我的单词库&quot;。</p>
<p>&nbsp;</p>
      <h4>3.11 我的学习记录</h4>
      <p>&quot;我的学习记录&quot;分门别类地记录您学习过的单词。目的是让您能随时复习前面学习过的单词。</p>
      <p>&nbsp;</p>
      <h4>3.12 环境保存 </h4>
      <p> 环境保存功能总是自动保存您上次学习的全部环境。当您中断学习后，再次进入《读我背单词时》，您学习的环境几乎同上次一<br />
      样。这会大大减少您不必要的操作。</p>
