    <h4>3.14 背单词的最大难度——坚持</h4>
    <p> 背单词是学好英语的必经步骤，这是每一个学习英语的用户都非常清楚的。背单词的最大困难不是缺少好的工具，而是难以坚持。每个人都知道，每天坚持背诵20个单词，一年就能掌握7300个单词。如果您能把7300个单词掌握得很透彻，那您的英语可以说好的塌糊涂。问题是很少有人能坚持。心血来潮可以让您坚持3天，外人忽悠可以让您坚持15天，面对考试可以让您坚持一个月，有强人约束可以让您坚持2个月。目前好象很难找到方法办法让您坚持一年风雨无阻地背单词。
</p>
      
    <p>&nbsp; </p>
    <p>我们正在研究通过网络技术解决您的坚持问题。也许我们无法让您坚持一年，但我们坚信我们能让你坚持两个月。坚持两个月，虽然无法让您的英语完全过关，但可以让您的英语提高一大步。<br />
    </p>
    <p>&nbsp;</p>
    <h3>4.使用《读我背单词》的建议学习步骤</h3>

<h4> 4.1 总体步骤</h4>
    <p> 第一步：确定您要背诵的单词集。<br />
第二步：利用背单词功能把所选单词集中的全部生单词初步背会。<br />
第三步：利用捋发音、捋词义或者捋拼写，把所选单词集中的全部单词记熟。</p>

<p>&nbsp;</p>
      <h4>4.2 背单词的步骤</h4>
      <p> 第一步：选择课程。背单词的课程必须是叶子课程。<br />
第二步：选择生单词。建议每次选择20个单词。<br />
第三步：对所选单词进行跟读练习。<br />
第四步：在跟读每个单词前，一定要查词典，仔细阅读单词的释义、用法、例句等。这对你能记牢单词非常关键。目前的在线词典非常多。客户端版的有金山词霸、友道词典；如果您不想下载，您可以直接使用百度词典。我们正在考虑让用户轻松<br />
而生动地记住单词的释义和用法的方法，不久会实现。<br />
第五步：做朗读练习。做完朗读练习后，您对所选的单词应该是基本临时掌握了。<br />
第六步：做词义练习和拼写练习以进一步巩固。<br />
第七步：用单词做造句练习。许多英语教学专家认为，造句练习是学习语言最有效的方法。我们以后会提供这方面的功能。<br />
第八步：做完以上步骤后，可以说您对所选生单词是真正掌握了，尽管记忆还很不牢固。<br />
</p>
  <p>&nbsp;</p>
      <h4>4.3 捋单词的步骤</h4>
      <p> 第一步：选择课程。该课程中的大多数单词应该是您已经初步掌握的。<br />
第二步：开始捋单词。建议您捋发音，尽管难度比其他两种练习大，但当您捋完后，您掌握的程度更好。</p>
