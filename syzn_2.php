          <h3>3.《读我背单词》的功能特征</h3>
          <h4>3.1 主要特点</h4>
          <p>a. 朗读单词是核心。《读我背单词》独特的朗读练习方式可以轻易帮您记住单词发音，或者纠正单词发音。</p>
          <p>b. "捋"——一种高效的单词记忆机制。</p>
          <p>c. 适合不同用户群及其不同需要的的超全分类单词库。</p>
          <p>d. 朗读单词是核心。《读我背单词》独特的朗读练习方式可以轻易帮您记住单词发音，或者纠正单词发音。</p>
      <p>&nbsp;</p>
       <h4> 3.2 朗读单词的好处和重要性 </h4>
       <p>a. 读单词不仅是练好口语的基础，而且是练好听力的基础。</p>
       <p>b. 朗读单词使您对单词的记忆更牢固、更持久。</p>
       <p>c. 朗读单词可以大大提高您的英语听力。只听不说，听力提高很慢。您可以试着听一段快速英语，您会发现：会正确说的单词，您都能听见 ；而不会说的单词基本听不到。 </p>
       <p>d. 朗读单词可以提高您对英语发音的准确性和敏感性。无论您花多少时间练习音标，您会发现您的发音进步很小；但是如果您利用朗读练习功能把200个单词朗读三遍，您会发现您对英语发音变得非常敏感。</p>
       <p>e. 朗读单词可以改进你的英语思维，提高您在听说读写方面的反映速度。</p>
       <p>f. 拿唱歌做比喻，一首歌听几十遍后，您可能仍然不会唱；但试唱几遍后，歌词和旋律就全记住了。这就是朗读单词的重要性。</p>
       
      <h4>&nbsp;</h4>
      <h4>3.3 功能层次结构图</h4>
      <p>《读我背单词》按照单词记忆机制不同分为两大功能：背单词和捋单词。因为单词的记忆包括三个方面：发音、词义和拼写，所以它们又包括朗读练习、拼写练习、词义练习等几个记忆练习环节。 </p>
