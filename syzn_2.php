      <h3>3.4 捋单词和背单词的区别 </h3>
<p>a. 背单词针对用户选择的单词进行各种练习；捋单词则针对用户选择的课程，系统根据用户对所选课程中单词的熟悉程度自动为用户挑选单词进行特定的练习。</p>
<p>b. 背单词更象是平时学习；而捋单词更象是总复习。</p>
      <p>c. 背单词的目的是把生单词初步掌握；捋单词的目的则是把初步掌握的单词变得滚瓜烂熟。</p>
