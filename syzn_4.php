      <p>a. 对于一个单词进行初试。</p>
       <p>b. 如果初试正确，过192小时后转到i；如果初试错误，过24小时后转到c。<br />
       </p>
       <p>c. 对该单词进行即时复习。<br />
       </p>
       <p>d. 如果即时复习正确，过48小时后转到e; 如果即时复习错误，过24小时后转到c。<br />
       </p>
       <p>e. 对该单词进行第一次复试。</p>
       <p>f. 如果第一次复试正确，过96小时后转到g; 如果第一次复试错误，过24小时后转到c。</p>
       <p>g. 对该单词进行第二次复试。<br />
       </p>
       <p>h. 如果第二次复试正确，过192小时后转到i; 如果第二次复试错误，过24小时后转到c。<br />
      </p>
       <p>i. 对该单词进行终试。</p>
       <p>j. 如果终试正确，该单词背诵过程结束; 如果终试错误，过24小时后转到c。</p>
