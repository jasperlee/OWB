          <h3>6.注册与登录</h3>
      <h4>6.1 登录界面：</h4>
      <p>当您启动程序后，首先进入登录界面（如图1）。登录界面包含如下元素：</p>
      <p>&nbsp;</p>

     <div class="zn_pic">
       <P><img src="image/zn_pic4.jpg" width="646" height="309" /></P> 
