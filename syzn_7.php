      <h4>&nbsp;</h4>
      <h4>&nbsp;</h4>
      <h4>用户帐号下拉列表框 </h4>
<p>用于输入或者选择用户帐号，用户帐号是一个邮箱地址。第一次登录时，您必须手动输入您的帐号 。每次在您的电脑上用新帐号登录，系统都会把它保存到该下拉列表中，下次登录时则可从该列表中选择登录帐号。</p>
<h4>用户密码编辑框 </h4>
<p> 用于输入用户密码。</p>
<h4>记住密码检查框 </h4>
<p>当您勾选了该检查框时，如果登录成功，针对该登录帐号的用户密码将会保存在您的电脑中，下次登录时，您可以省去密码输入过程。</p>
<p>注意：请只在您自己单独使用的电脑中设置该检查框。</p>
<h4>注册帐号按钮</h4>
<p>如果您现在还没有自己的帐号，或者您想注册一个新帐号，请点此按钮。</p>
<h4>忘记密码按钮 </h4>
<p>如果您忘记了您的帐号的密码，或者想修改密码，请点此按钮。</p>
<h4>试用按钮</h4>
<p>如果您只是想试用并了解《读我背单词》功能特征，而不想花时间去注册帐号，您可以点此按钮。</p>
<h4>确定按钮 </h4>
<p>当正确地输入了用户帐号和密码后，点此按钮开始登录。</p>
